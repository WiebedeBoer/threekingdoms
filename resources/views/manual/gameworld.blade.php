<h1 class="mai">Gameworld</h1>

<h2><a name="gameworld" class="anch">Gameworld</a></h2>
<p>The game world consists of most of China. It is a vast world with a great variety in resources, climates, and cultures.</p>
<p>Scattered throughout the world are 125 settlements. These settlements range from small towns to large cities.</p>

<h2><a name="timespan" class="anch">Special Structures</a></h2>

<h3>Courts</h3>
<p>Some of these settlements have courts of old kingdoms, prefectures, and commanderies, while most of the remaining settlements are ordinary counties. There is one settlement that has the imperial court.</p>

<h3>Workshops</h3>
<p>Some settlements have markets with workshops. These workshops produce special trade items. These trade items provide bonuses to statistics.</p>

<h3>Temples &amp; Tombs</h3>
<p>There are a few settlements that have old and grand temple complexes or ancient tombs. The tombs often are final resting places of famous ladies, lords, kings, and emperors. Most of these temples belong to one of the ancient hundred schools of thought, such as Confucianism, Taoism, Mohism, and Legalism.</p>

<h2><a name="timespan" class="anch">Timespan</a></h2>
<p>Time starts in the year 184 AD, at the time of the Yellow Turban Rebellion. It ends in the year 280 AD, when Jin conquered Wu.</p>
<p>In all a game lasts a maximum of 96 years. A game round begins during the Eastern Han Dynasty, lasting throughout the Three Kingdoms period, and ends during the Western Jin Dynasty.</p>
<h2><a name="known">The Known World</a></h2>
<p>The entire game world is known. Beyond the scope of the game world, there is some limited knowledge of other regions and civilizations outside of the game world. Some trade exists in precious resources with neighbouring regions.</p>
<h3>The Silk Road</h3>
<p>The Silk Road is established during the Han Dynasty, so it is a relatively recent trade route. Initially trade along the Silk Road is maintained by Bactrian and Sogdian traders. The Parthians and the Seleucids are some of the first foreign empires to come into contact with these traders. The Chinese did send an embassy to the Roman Empire, but little is known about Europe.</p>
<h3>Religions &amp; Philosophical Traditions</h3>
<p>Most people adhere to Confucianism or Taoism. Confunianism is the official ruling doctrine of the Han Dynasty, but Taoism which in some cases incorporates some folk religion and local Shamanistic practices is widespread. Other traditions of the Spring and Autumn era like Mohism and Chinese Legalism are in decline. Buddhism which came from India is an emerging religion and is being transmitted along the Silk Road. Buddhists have founded a temple in the city Luoyang, but Buddhism is rarely practiced among the population.</p>
<h2><a name="han" class="anch">Han Dynasty</a></h2>
<h3>Han Dynasty</h3>
<p>The Han Dynasty is the Imperial Dynasty that nominally rules China. In reality though, real power is in the hands of various warlords that rule parts of China. The Han empire has weakened through rebellions, droughts, economic decline, corruption in the imperial court and infighting.</p>
<h3>Imperial Court</h3>
<p>The Imperial Court of the Han Dynasty is initially located in the city of Luoyang. Warlords may capture the emperor and hold him hostage. So the imperial court can be moved to other cities. Warlords may also set up rival imperial courts of their own after the imperial court of the Han Dynasty has been eleminated.</p>

<h2><a name="rebels" class="anch">Rebels &amp; Barbarians</a></h2>

<p>An uprising occurs when happiness in a settlement gets too low. These rebels may take over a settlement. Rebels at the fringe of the map often are barbarians. There are three kinds of common rebels and several kinds of barbarians.</p>

<h3>Rebels</h3>

<p><b>Yellow Turbans</b>
<br>The Yellow Turbans are the most widespread rebels. They are primarily found in Wei county, but are also known to cooperate with traitors inside Luoyang. The rebels are led by Zhang Yue. Although initially the result of an agrarian uprising, the Yellow Turbans are in fact a Taoist sect.</p>

<p><b>Black Mountain Bandits</b>
<br>The Black Mountain Bandits are an offshoot of the Yellow Turbans. They are a bandit confederacy primarily found in the Taishan mountain range and conduct raids into the neighbouring commanderies.</p>

<p><b>Five Pecks of Rice</b>
<br>The Way of the Five Pecks of Rice is a small rebel theocratic state located in the Hanzhong valley. The Way of the Five Pecks of Rice is founded by master Zhang Daoling.</p>

<h3>Barbarians</h3>
<p>Several barbarian tribes and barbarian kingdoms can be found on the fringes of the game world.</p>

<p><b>Koguryo</b>
<br>Koguryo are Tungusic peoples located in the northern and central parts of the Korean Peninsula is one of the ancient Three Kingdoms of Korea. In the chaos following the fall of the Han Dynasty, the former Han commanderies had broken free of control and were ruled by various independent warlords. Koguryo moved into this local power vacuum.</p>

<p><b>Wuhuan</b>
<br>The Wuhuan are a proto-Mongolic nomadic people who inhabited northern China. They are remnants of a tribal confederation called the Donghu.</p>

<p><b>Xiongnu</b>
<br>The Xiongnu are an ancient proto-Mongolic nomadic people who form a state or confederation centered on current Mongolia. They are the dominant power on the northern steppes.</p>

<p><b>Yuezhi</b>
<br>The Yuezhi are Tocharians or an ancient Indo-European people. They originally settled in the arid grasslands of the Tarim Basin area. Their power waned following defeats by the Xiongnu. They migrated to the Bactrian region where one branch of the Yuezhi founded the Kushan Empire. They are a nomadic people who trade in horses and jade.</p>

<p><b>Qiang</b>
<br>The Qiang are an acient Tibetan people live mainly in the northwestern mountainous region near Tibet. The Qiang have a matrilineal society with ancient shamanistic beliefs. The majority of the Qiang adhere to a polytheist religion. Both the men and women wear intricate gowns. The Qiang hunt animals, collect mushrooms and herbs in the forests, and herd yaks and horses on the mountain-top pastures.</p>
<p><b>Baima</b>
<br>The Baima people is a subgroup of Tibetan peoples, living in the extreme western fringes, near the Tibetan plateau. They believe in Taoism and Buddhism, but also worship totems.</p>

<p><b>Minyue</b>
<br>The Minyue people is a subgroup of Baiyue peoples. The Minyue are a matrilocal society with some matrilineal tendencies. Their beliefs include the worship of totems and the practice of cliff burials. They live in the southeastern parts of China. Minyue was partially conquered by the Han Dynasty by the end of the second century. They were subject to the Nanyue to the south of them. Due to its closed off position, the Han Dynasty was unable to establish a strong grip over the area they inhabited.</p>
<p><b>Nanyue</b>
<br>The Nanyue people is a subgroup of Baiyue peoples. The Nanyue was an ancient kingdom, located in the southern parts of China and northern Vietnam. Although subject to the Han Empire and partially assimilated, they retained some degree of autonomy.</p>
<p><b>Yelang</b>
<br>The Yelang people is a subgroup of Baiyue peoples. They live in the southwestern parts of China, in the vicinity of the Yunnan region. The Yelang are an alliance of tribes. Yelang had a close relationship with Nanyue. The Yelang are a sedentary farming people. The Yelang have a unique burial custom, in which the head of the deceased is placed into a bronze pot.</p>
<p><b>Dianyue</b>
<br>The Dianyue people is a subgroup of Baiyue peoples. They live in the southwestern parts of China, in the vicinity of the Yunnan region. The Dian were subjugated by the Han Dynasty. The Kingdom is based on agriculture, but the Dianyue are also sophisticated metal workers, casting many bronze and iron figurines. The Dian bury their dead in vertical pit graves.</p>


<h2><a name="anachronisms" class="anch">Anachronisms &amp; Roleplay</a></h2>
<p>For roleplay purposes you should be aware that some crops, animals, and peoples we know today, were unknown in ancient China. To keep into the spirit of the era, please only use characters, items, etcetera, that fit within the timespan and the known world.</p>
<h3>Unknown</h3>
<p>The Mongols did not start to become a menace until centuries later. If you want to play a nomad horde you should pick something like the barbarian Xiongnu.</p>
<p>Plants like the potato, tomato, squash, and grapefruit were introduced during the Columbian Exchange. These plants are native to the Americas, which have not yet been discovered.</p>
<p>Things like the famous Chinese porcelain vases were not yet manufactured. Although a lot of ceramics were already present during the Han dynasty, the precious porcelain vases did not come available until the Ming Dynasty.</p>
